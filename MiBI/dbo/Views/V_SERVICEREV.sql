CREATE VIEW dbo.V_SERVICEREV
AS
SELECT     dbo.PRODUCT_LINE.ProductLine, dbo.CUSTOMER_PRODUCT_DATA.Sku, dbo.CUSTOMER_PRODUCT_DATA.PartnerG AS Partner, 
                      dbo.CUSTOMER_PRODUCT_DATA.EndCustName AS Customer, dbo.CUSTOMER_PRODUCT_DATA.CustomerPo, 
                      dbo.CUSTOMER_PRODUCT_DATA.NetUSD AS Revenue, dbo.TimeLookup.FQuarter, dbo.TimeLookup.theMonth
FROM         dbo.CUSTOMER_PRODUCT_DATA INNER JOIN
                      dbo.PRODUCT_LINE ON dbo.CUSTOMER_PRODUCT_DATA.Sku = dbo.PRODUCT_LINE.sku INNER JOIN
                      dbo.TimeLookup ON CONVERT(Char(10), dbo.CUSTOMER_PRODUCT_DATA.ShipDate, 101) = CONVERT(CHAR(10), dbo.TimeLookup.theDate, 101)
WHERE     (dbo.PRODUCT_LINE.Type = 'Service')

