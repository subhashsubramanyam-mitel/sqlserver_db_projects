
CREATE VIEW dbo.V_Tableau_MRT
AS
SELECT [ID]
      ,[Status]
      ,[SubStatus]
      ,[ChangeList]
      ,[CorrectedInID]
      ,[CreatedBy]
      ,[CreatedByID]
      ,[CreatedDate]
      ,[DefectID]
      ,[DefectAbstract]
      ,[DeliveryVehicle]
      ,[DueDate]
      ,[Effort]
      ,[InKnowledgeBase]
      ,[LastModifiedBy]
      ,[LastModifiedByID]
      ,[LastModifiedDate]
      ,[MRTName]
      ,[QAAQAC]
      ,[RecordTypeID]
      ,[RejectedDate]
      ,[RelatedCases]
      ,[ResolvedDate]
      ,[ReviewedByID]
      ,[ReviewedDate]
      ,[Risk]
      ,[SEDefectNum]
      ,[TargetID]
      ,[TestEngineer]
      ,[TestEngineeremail]
      ,[VerifiedDate]
      ,[VerifiedIn]
