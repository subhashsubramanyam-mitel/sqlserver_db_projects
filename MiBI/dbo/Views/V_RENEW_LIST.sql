CREATE VIEW [dbo].[V_RENEW_LIST]
AS
SELECT     b.Partner_Csn, a.Type, convert(Char(10),a.EndDate, 101) as ExpireDate, a.Status, b.NAME, b.SupportType, c.NAME AS VAR, Disty.NAME AS VAD, e.FName + ' ' + e.LName AS Contact, 
                      e.Email
FROM         dbo.CUSTOMER_AGREEMENTS AS a INNER JOIN
                      dbo.CUSTOMERS AS b ON a.End_User_Csn = b.End_User_Csn INNER JOIN
                      dbo.RESELLER AS c ON b.Partner_Csn = c.Partner_Csn LEFT OUTER JOIN
                      dbo.CONTACTS AS e ON b.End_User_Csn = e.AccountId LEFT OUTER JOIN
                      dbo.RESELLER AS Disty ON c.Parent_Csn = Disty.Partner_Csn
WHERE     (a.Status = 'Current') AND (a.Type LIKE 'Enterprise%' OR
                      a.Type LIKE '%Partner%') AND (a.EndDate <= GETDATE() + 90) AND (e.CorpSpnsr = 'Y')

